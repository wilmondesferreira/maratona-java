package academy.devdojo.maratonajava.introducao;

    //Tipos Primitivos sao palavras reservadas que guardam valores simples
    //Java: int, double, float, chat, byte, short, long, boolean

    /*
    A maioria dos tipos primitivos sao numericos, excecao do boolean que possui 2 valores (true e false)
    as diferencas entre os tipos primitivos numericos sao as capacidades que cada um armazena
     */

public class Aula02_TiposPrimitivos {
    public static void main(String[] args) {

        int idade = 10; //armazena números inteiros de -2.147.483.648 a 2.147.483.647.
        long numeroGrande = 100000; //armazena números inteiros de -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807.
        double salarioDouble = 2000.0; //armazena números de ponto flutuante com precisão dupla
        float salarioFloat = 2500; //armazena números de ponto flutuante
        byte idadeByte = 10; //armazena números inteiros de -128 a 127.
        short idadeShort = 10; // armazena números inteiros de -32.768 a 32.767.
        boolean verdadeiro = true; //armazena valores booleanos (verdadeiro ou falso).
        boolean falso = false;
        char caractere = 'M'; //armazena caracteres únicos utilizando o padrão Unicode
        String nome = "Texto dentro de uma string";







    }
}
